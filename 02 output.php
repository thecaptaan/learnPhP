<?php

// echo() - print single or multiple values
echo 'Hello, world!', 123;

// print() - only print single value
print 123;

// print_r() - print single or values of array
print_r('2','3');

// var_dump() - print single or multiple values with data type
var_dump(0);

// var_export() - similar to var_dump() but it returns the output a string rep of the variable
var_export(1);

?>